/*------------------------------------------------------------------------------
	インクルードファイル
------------------------------------------------------------------------------*/
#include "Shape.h"
#include "Wall.h"
#include "Roof.h"
#include "DebugLog.h"

/*------------------------------------------------------------------------------
	デストラクタ
------------------------------------------------------------------------------*/
Shape::~Shape()
{
	Uninit();
}

/*------------------------------------------------------------------------------
	終了処理
------------------------------------------------------------------------------*/
void Shape::Uninit( void)
{
	//屋根の消去
	for (auto roof : m_Roofs)
	{
		delete roof;
	}
	m_Roofs.clear();

	//壁の消去
	for (auto wall : m_Walls)
	{
		delete wall;
	}
	m_Walls.clear();
}

/*------------------------------------------------------------------------------
	屋根の追加
------------------------------------------------------------------------------*/
void Shape::AddRoof(Roof* roof)
{
	m_Roofs.push_back( roof);
}

/*------------------------------------------------------------------------------
	屋根の削除
------------------------------------------------------------------------------*/
void Shape::SubRoof(Roof* roof)
{
	for (auto ite = m_Roofs.begin(); ite != m_Roofs.end(); ++ite)
	{
		if (*ite == roof)
		{
			delete roof;
			m_Roofs.erase( ite);
			return;
		}
	}

	DebugLog::Add( "ShapeにRoofが見つからない\n");
}

/*------------------------------------------------------------------------------
	壁の追加
------------------------------------------------------------------------------*/
void Shape::AddWall( Wall* wall)
{ 
	m_Walls.push_back( wall);
}

/*------------------------------------------------------------------------------
	壁の削除
------------------------------------------------------------------------------*/
void Shape::SubWall( Wall* wall)
{ 
	for (auto ite = m_Walls.begin(); ite != m_Walls.end(); ++ite)
	{
		if (*ite == wall)
		{
			delete wall;
			m_Walls.erase( ite);
			return;
		}
	}

	DebugLog::Add( "ShapeにWallが見つからない\n");
}

/*------------------------------------------------------------------------------
	壁のクリア
------------------------------------------------------------------------------*/
void Shape::ClearWall( void)
{ 
	for (auto ite = m_Walls.begin(); ite != m_Walls.end(); ++ite)
	{
		delete *ite;
	}

	m_Walls.clear();
}

/*------------------------------------------------------------------------------
	マトリクスの取得
------------------------------------------------------------------------------*/
D3DXMATRIX Shape::GetMatrix(void)
{
	D3DXMATRIX matrix;
	D3DXMatrixIdentity( &matrix);
	D3DXMatrixTranslation( &matrix, m_Position.x, m_Position.y, m_Position.z);
	D3DXMatrixRotationY( &matrix, m_Rotation);

	return matrix;
}
