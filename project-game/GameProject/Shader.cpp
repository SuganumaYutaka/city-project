#include "Shader.h"

/*------------------------------------------------------------------------------
	終了処理
------------------------------------------------------------------------------*/
void Shader::Uninit( void)
{
	//シェーダ情報格納インターフェイスの解放
	if (m_pEffect != NULL) {
		m_pEffect->Release();
		m_pEffect = NULL;
	}

	//頂点宣言格納インターフェイスの解放
	if( m_pVertexDec != NULL){
		m_pVertexDec->Release();
		m_pVertexDec = NULL;
	}
}






